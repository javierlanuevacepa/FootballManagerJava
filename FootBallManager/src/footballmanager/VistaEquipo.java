    public void RenderizarVista(){
        NombreEquipo.setText(wor.getEquipoPlayer().NombreEquipo);
        Fans.setText("Fans:"+wor.getEquipoPlayer().Fans);
        Capa.setText("Cap. Estadio:"+wor.getEquipoPlayer().EstEquipo.Capacidad);
        Forma.setText("Formacion:"+wor.getEquipoPlayer().getFormacion());
        Edad.setText("Edad media:"+wor.getEquipoPlayer().getEdadMedia());
        Valor.setText("Valor total plantel:$"+wor.getEquipoPlayer().getValorTotalPlantel());
        Ataque.setText(String.valueOf(wor.getEquipoPlayer().calcularAtaque()));
        Medio.setText(String.valueOf(wor.getEquipoPlayer().calcularMedioCampo()));
        Dfe.setText(String.valueOf(wor.getEquipoPlayer().calcularDefensa()));
        Porte.setText(String.valueOf(wor.getEquipoPlayer().calcularPorteria()));
        Pg.setText(String.valueOf(wor.getEquipoPlayer().EstadisticasLiga.PartidosGanados));
        Pe.setText(String.valueOf(wor.getEquipoPlayer().EstadisticasLiga.PartidosEmpatados));
        Pp.setText(String.valueOf(wor.getEquipoPlayer().EstadisticasLiga.PartidosPerdidos));
        Punt.setText(String.valueOf(wor.getEquipoPlayer().EstadisticasLiga.Puntos));
        Ga.setText(String.valueOf(wor.getEquipoPlayer().EstadisticasLiga.GolesAnotados));
        Ge.setText(String.valueOf(wor.getEquipoPlayer().EstadisticasLiga.GolesEncontra));
        TablaPos.setText("Tabla de posiciones "+wor.getEquipoPlayer().EstadisticasLiga.NombreLigaOTorneo);
        Fondos.setText("Fondos:$"+wor.getEquipoPlayer().Dinero);
        
        
        String AgnoTemporada = wor.Agno+"-"+Integer.valueOf(String.valueOf(wor.Agno+1));
        
        Temp.setText("Temporada "+AgnoTemporada);
        
        
        
        
    }
    
    
    
    
