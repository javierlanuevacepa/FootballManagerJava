    public void Sorting(){
        for (Equipo x : LigaSe.Equipos){
            EquipesTable.add(new EquiposTableView(x.NombreEquipo,x.EstadisticasLiga.PartidosGanados,x.EstadisticasLiga.PartidosPerdidos,x.EstadisticasLiga.PartidosEmpatados,x.EstadisticasLiga.GolesAnotados,x.EstadisticasLiga.GolesEncontra,x.EstadisticasLiga.Puntos));
        }
        Collections.sort(EquipesTable, new Comparator<EquiposTableView>(){
            @Override
            public int compare(EquiposTableView p1, EquiposTableView p2) {
                return Integer.compare(p2.Puntos, p1.Puntos); // Para orden descendente
            }
        });
    }

