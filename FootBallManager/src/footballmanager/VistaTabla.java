    public void RenderizarVista(){
        
        NombreEquipo.setText(wor.getEquipoPlayer().NombreEquipo);
        
        
        Sorting();
        
        DefaultTableModel modelo = new DefaultTableModel();
       
        modelo.addColumn("Equipo");
        modelo.addColumn("PG");
        modelo.addColumn("PP");
        modelo.addColumn("PE");
        modelo.addColumn("GA");
        modelo.addColumn("GC");
        modelo.addColumn("Puntos");
        
        while (modelo.getRowCount()>0){
            modelo.removeRow(0);
        }
        
        for (EquiposTableView ekip : EquipesTable){
            
            Object a[] = new Object[7];
            
            a[0] = ekip.Equipo;
            a[1] = ekip.PG;
            a[2] = ekip.PP;
            a[3] = ekip.PE;
            a[4] = ekip.GA;
            a[5] = ekip.GC;
            a[6] = ekip.Puntos;
            
            modelo.addRow(a);
        }
        
        Tablis.setModel(modelo);
        
        
    }
    
    
    
    
