    private void TablisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablisMouseClicked
        // TODO add your handling code here:
        if (Tablis.getSelectedRow()!=-1){
            
        
        
        for (Equipo x : LigaSe.Equipos){
            if (x.NombreEquipo.equals(LigaSe.Equipos.get(Tablis.getSelectedRow()).NombreEquipo)){
                Model = x;
            }
        }
        
        
        
        
        Equipe.setText(Model.NombreEquipo);
        Fans.setText("Fans:"+wor.getEquipoPlayer().Fans);
        Capa.setText("Cap. Estadio:"+Model.EstEquipo.Capacidad);
        Forma.setText("Formacion:"+Model.getFormacion());
        Edad.setText("Edad media:"+Model.getEdadMedia());
        Valor.setText("Valor total plantel:$"+Model.getValorTotalPlantel());
        Ataque.setText(String.valueOf(Model.calcularAtaque()));
        Medio.setText(String.valueOf(Model.calcularMedioCampo()));
        Dfe.setText(String.valueOf(Model.calcularDefensa()));
        Porte.setText(String.valueOf((int) Model.calcularPorteria()));
        Pg.setText(String.valueOf(Model.EstadisticasLiga.PartidosGanados));
        Pe.setText(String.valueOf(Model.EstadisticasLiga.PartidosEmpatados));
        Pp.setText(String.valueOf(Model.EstadisticasLiga.PartidosPerdidos));
        Punt.setText(String.valueOf(Model.EstadisticasLiga.Puntos));
        Ga.setText(String.valueOf(Model.EstadisticasLiga.GolesAnotados));
        Ge.setText(String.valueOf(Model.EstadisticasLiga.GolesEncontra));
        }
    }//GEN-LAST:event_TablisMouseClicked

