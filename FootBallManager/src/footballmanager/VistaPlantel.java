    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (Eleccion!=3 && IndexJug!=999){
            switch (Eleccion){
                case 0 ->{
                    String[] Opciones = {"Cambiar jugador","Arrepentirse"};
                    JComboBox opciones = new JComboBox(getJugadoresLista(1));
                    int IndexCambio = JOptionPane.showOptionDialog(null, opciones,"Elige el jugador",JOptionPane.DEFAULT_OPTION,JOptionPane.PLAIN_MESSAGE,null,Opciones,
                Opciones[0]);
                    
                    if (IndexCambio == 0){
                    
                    if (wor.getEquipoPlayer().OnceInicial.get(IndexJug).Posicion.equals("Arquero") && !wor.getEquipoPlayer().Reserva.get(opciones.getSelectedIndex()).Posicion.equals("Arquero")){
                        JOptionPane.showMessageDialog(null, 
                              "Al cambiar el arquero debes hacerlo por otro arquero!!", 
                              "!!!", 
                              JOptionPane.WARNING_MESSAGE);
                    }else{
                        wor.getEquipoPlayer().CambioJugador(IndexJug, opciones.getSelectedIndex());
                        RenderizarVista();
                        Eleccion = 3;
                        IndexJug = 999;
                        clearFields();
                    }
                    }
                }
                case 1 ->{
                    String[] Opciones = {"Cambiar jugador","Arrepentirse"};
                    JComboBox opciones = new JComboBox(getJugadoresLista(0));
                    int IndexCambio = JOptionPane.showOptionDialog(null, opciones,"Elige el jugador",JOptionPane.DEFAULT_OPTION,JOptionPane.PLAIN_MESSAGE,null,Opciones,
                Opciones[0]);
                    if (IndexCambio == 0){
                    if (wor.getEquipoPlayer().Reserva.get(IndexJug).Posicion.equals("Arquero") && !wor.getEquipoPlayer().OnceInicial.get(opciones.getSelectedIndex()).Posicion.equals("Arquero")){
                        JOptionPane.showMessageDialog(null, 
                              "Al cambiar el arquero debes hacerlo por otro arquero!!", 
                              "!!!", 
                              JOptionPane.WARNING_MESSAGE);
                    }else{
                        wor.getEquipoPlayer().CambioJugador(opciones.getSelectedIndex(), IndexJug);
                        RenderizarVista();
                        Eleccion = 3;
                        IndexJug = 999;
                        clearFields();
                    }
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

