    public String[] getJugadoresLista(int Elecc){
        switch (Elecc){
            case 0 ->{
                ArrayList<String> Jugadores = new ArrayList<>();
                
                for (Futbolista nomb : wor.getEquipoPlayer().OnceInicial){
                    Jugadores.add(nomb.Nombre+" "+nomb.Apellido+"("+nomb.Posicion+")");
                }
                return Jugadores.toArray(String[]::new);
            }
            case 1 ->{
                ArrayList<String> Jugadores = new ArrayList<>();
                
                for (Futbolista nomb : wor.getEquipoPlayer().Reserva){
                    Jugadores.add(nomb.Nombre+" "+nomb.Apellido+"("+nomb.Posicion+")");
                }
                return Jugadores.toArray(String[]::new);
            }
        }
        return null;
    }
    
