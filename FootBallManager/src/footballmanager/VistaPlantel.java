    public void RenderizarVista(){
        NombreEquipo.setText(wor.getEquipoPlayer().NombreEquipo);
        
        Eleccion = 3;
        IndexJug = 999;
        Jugador2.setText("");
        
        OnceInicial.removeAll();
        
        int Indies = 0;
        
        for (Futbolista x : wor.getEquipoPlayer().OnceInicial){
            VistaJugador vivi = new VistaJugador();
            vivi.Ajustar(x);
            int Indos = Indies;
             MouseListener ml = new MouseAdapter(){
                    
                    @Override
                    public void mousePressed(MouseEvent e){
                        Eleccion = 0;
                        IndexJug = Indos;
                        
                        Jugador2.setText(x.Nombre+" "+x.Apellido+"("+x.Posicion+")");
                        AjustarGagrit(x);
                    }
                };
                vivi.addMouseListener(ml);
            Indies++;
            OnceInicial.add(vivi);
        }
        Indies = 0;
        Reservas.removeAll();
        for (Futbolista y : wor.getEquipoPlayer().Reserva){
            VistaJugador vivi = new VistaJugador();
            vivi.Ajustar(y);
            
            int Indos = Indies;
             MouseListener ml = new MouseAdapter(){
                    
                    @Override
                    public void mousePressed(MouseEvent e){
                        Eleccion = 1;
                        IndexJug = Indos;
                        
                        Jugador2.setText(y.Nombre+" "+y.Apellido+"("+y.Posicion+")");
                        AjustarGagrit(y);
                    }
                };
                vivi.addMouseListener(ml);
            Indies++;
            OnceInicial.add(vivi);
            
            
            
            Reservas.add(vivi);
        }
        
        EntRestantes.setText("Entrenamientos restantes:"+wor.getEquipoPlayer().EntrenosPorEquipo);
      
        OnceInicial.updateUI();
        Reservas.updateUI();
        
    }
    
    
    
    
